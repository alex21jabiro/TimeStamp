     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $business = new Business;
        $business->bus_name = request('business_name');
        $business->bus_address = request('street');
        $business->bus_city = request('city');
        $business->bus_state = request('state');
        $business->bus_zip = request('zip');
        $business->bus_tax_id = request('tax_id');
        $business->bus_phone = request('phone');
        $business->bus_email = request('email');
        
        $business->save();
        
        return redirect('/paymentselection');
    }

